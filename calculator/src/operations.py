def add(a, b):
    return a + b

#Operación: División
def divide(a, b):       
    if b == 0: #Si el divisor es 0, retorna un mensaje de error
        return "Error: Division by zero"    
    return (a / b) #Retorna el resultado de la división para cualquier otro caso 


def multiply(a, b):
    return a * b

def subtract(a, b):
    return a - b 
